#!/usr/bin/env node
// Extracts frames from a video file into assets/video_frames/ and regenerates
// the frames index module consumed by the Video Replay screen.
//
// Usage:
//   node scripts/extract-video-frames.mjs <video.mp4> [--fps 5] [--width 640]
//
// Requires ffmpeg on PATH.

import { execFileSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const argv = process.argv.slice(2);
const videoPath = argv.find((a) => !a.startsWith('--'));

const readOption = (name, fallback) => {
  const idx = argv.indexOf(`--${name}`);
  if (idx === -1 || idx + 1 >= argv.length) return fallback;
  const value = Number(argv[idx + 1]);
  if (!Number.isFinite(value) || value <= 0) {
    console.error(`Invalid value for --${name}: ${argv[idx + 1]}`);
    process.exit(1);
  }
  return value;
};

if (!videoPath || !fs.existsSync(videoPath)) {
  console.error(
    'Usage: node scripts/extract-video-frames.mjs <video.mp4> [--fps 5] [--width 640]'
  );
  process.exit(1);
}

const fps = readOption('fps', 5);
const width = readOption('width', 640);

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const outDir = path.join(scriptDir, '..', 'assets', 'video_frames');
fs.mkdirSync(outDir, { recursive: true });

for (const file of fs.readdirSync(outDir)) {
  if (/^frame_\d+\.jpg$/.test(file)) {
    fs.unlinkSync(path.join(outDir, file));
  }
}

console.log(`Extracting frames at ${fps} fps (width ${width}px)...`);
execFileSync(
  'ffmpeg',
  [
    '-hide_banner',
    '-loglevel', 'error',
    '-i', videoPath,
    '-vf', `fps=${fps},scale=${width}:-2`,
    '-q:v', '3',
    path.join(outDir, 'frame_%04d.jpg'),
  ],
  { stdio: 'inherit' }
);

const frameFiles = fs
  .readdirSync(outDir)
  .filter((file) => /^frame_\d+\.jpg$/.test(file))
  .sort();

if (frameFiles.length === 0) {
  console.error('ffmpeg produced no frames.');
  process.exit(1);
}

const indexLines = [
  '// AUTO-GENERATED by scripts/extract-video-frames.mjs — do not edit.',
  `export const fps = ${fps};`,
  'export const frames: number[] = [',
  ...frameFiles.map((file) => `  require('./${file}'),`),
  '];',
  '',
];
fs.writeFileSync(path.join(outDir, 'index.ts'), indexLines.join('\n'));

console.log(`Wrote ${frameFiles.length} frames to ${outDir}`);
